#include <iostream>
using namespace std;


int main() {
    int length, width;
    cout << "Enter length: ";
    cin >> length;
    cout << "Enter width: ";
    cin >> width;
    cout << "Length is: " << length << endl;
    cout << "Width is: " << width << endl;
    cout << "Area is: " << length * width << endl;
    return 0;
}